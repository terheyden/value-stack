package com.terheyden.value;

/**
 * TestAnimal class.
 */
public class TestAnimal {

    private final String name;
    private final int age;

    public TestAnimal(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }
}
